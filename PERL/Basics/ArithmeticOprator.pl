#!/usr/bin/perl

use strict;
use warnings;

my $num1=10;
my $num2=2;

my $sum = $num1 + $num2;
my $diffrence= $num1 - $num2;
my $product= $num1 * $num2;
my $quotient= $num1 / $num2;
my $remainder= $num1 & $num2;
my $expo= $num1 ^ $num2;

print "Sum : $sum\n Diffrence : $diffrence\n Product : $product\n Quotient : $quotient\n Remainder : $remainder\n Exponential :$expo\n";

